    private void rejectBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rejectBtnMouseClicked
        int reply = JOptionPane.showConfirmDialog(null, "Are you sure want to reject this comment", "WARNING", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
            try {
                // TODO add your handling code here:
                int id_comment = listComments.get(commentTable.getSelectedRow()).getId();
                Database db = new Database();
                if(db.deleteComment(id_comment)){
                    commentTable.revalidate();
                    commentTable.repaint();
                    this.display();
                    System.out.println("berhasil delete comment");
                }else{
                    System.out.println("gagal delete comment");
                }
            }catch (ClassNotFoundException ex) {
                Logger.getLogger(CommentsRatings.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Comment rejected successfully!", "INFO", JOptionPane.INFORMATION_MESSAGE); 
        }
    }//GEN-LAST:event_rejectBtnMouseClicked

