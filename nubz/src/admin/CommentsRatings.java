    public DefaultTableModel showComments() throws ClassNotFoundException{
        this.revalidate();
        this.repaint();
        
        DefaultTableModel dtm = new DefaultTableModel(new Object[][]{}, new String []{
        "No", "Comment", "Nama Smartphone" , "Status"
        });
        Database db = new Database();
        if(this.jComboBox1.getSelectedItem().toString() == "Pending Comments"){
            this.listComments = db.getComment();
        }else{
            this.listComments = db.getAllComment();
        }
        
        for(int i=0; i<this.listComments.size(); i++){
            String status = "";
            if(this.listComments.get(i).getStatus() == 1){
                status = "approved";
            }else{
                status = "pending";
            }
            Object o = new Object[]{i+1,this.listComments.get(i).getComment(), this.listComments.get(i).getNama(), status
            };
            dtm.addRow((Object[]) o);
        }
        return dtm;
    }

