    private void approveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_approveBtnMouseClicked
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            int id_comment = listComments.get(commentTable.getSelectedRow()).getId();
            Database db = new Database();
            if(db.approveComment(id_comment)){
                commentTable.revalidate();
                commentTable.repaint();
                this.display();
                System.out.println("berhasil approve comment");
            }else{
                System.out.println("gagal approve comment");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CommentsRatings.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Comment approved successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE); 
    }//GEN-LAST:event_approveBtnMouseClicked
